    static boolean isValid(int[] array) {

        if (array == null) {
            return false;
        }

        if (array.length < 1) {
            return false;
        }

        return true;
    }

    static int getMediumValue (int[] array) {

        int min = 0;
        int max = 0;

        for (int arr : array) {

            if (arr < min) {
                 min = arr;
            }

            if (arr > max) {
                max = arr;
            }
        }

        return (min + max) / 2;
    }

    static int getSorting (int[] array, int left, int right) {

        int separator = getMediumValue(array);
        int temp;

        while (array[left] < separator) {
            left++;
        }

        while (array[right] > separator) {
            right--;
        }

        if (left <= right) {
            temp = array[left];
            array[left] = array[right];
            array[right] = temp;
        }

        return right;
    }
