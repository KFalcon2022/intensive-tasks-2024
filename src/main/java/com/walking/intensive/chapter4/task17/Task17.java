        if (!isValid(array)) {
            return new int[0];
        }

        int tempValue;
        int len = array.length - 1;

        for (int j = len; j >= 1; j--) {

            for (int i = 0; i < len; i++) {

                if (array[i] > array[i + 1]) {
                    tempValue = array[i];
                    array[i] = array[i + 1];
                    array[i + 1] = tempValue;
                }
            }
        }

