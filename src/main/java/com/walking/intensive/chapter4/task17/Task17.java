    public static int[] quickSort(int[] arr, int left, int right){
        if(left < right){
            int p = arr[(left + right) / 2];
            int i = left;
            int j = right;

            while (i<=j){
                while (arr[i] < p){
                    i++;
                }
                while (arr[j] > p){
                    j--;
                }
                if(i<=j){
                    int tmp = arr[i];
                    arr[i] = arr[j];
                    arr[j] = tmp;
                    i++;
                    j--;
                }
            }

            quickSort(arr,left, j);
            quickSort(arr,i, right);
        }
        return arr;
    }
