package com.walking.intensive.chapter5.task19;

public class Sphere {
    private final int x, y, z, r;

    public Sphere(int x, int y, int z, int r) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.r = r;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }

    public int getR() {
        return r;
    }
}
